package com.baobao.common.model.andshow;

public class CountScore {
	
	private String memberName;
	
	private String branchName;
	
	private float actScore;
	
	private float taskScore;
	
	private float studyScore;
	
	private float allScore;

	public String getMemberName() {
		return memberName;
	}

	public void setMemberName(String memberName) {
		this.memberName = memberName;
	}

	public float getActScore() {
		return actScore;
	}

	public void setActScore(float actScore) {
		this.actScore = actScore;
	}

	public float getTaskScore() {
		return taskScore;
	}

	public void setTaskScore(float taskScore) {
		this.taskScore = taskScore;
	}

	public float getStudyScore() {
		return studyScore;
	}

	public void setStudyScore(float studyScore) {
		this.studyScore = studyScore;
	}

	public float getAllScore() {
		return allScore;
	}

	public void setAllScore(float allScore) {
		this.allScore = allScore;
	}

	public String getBranchName() {
		return branchName;
	}

	public void setBranchName(String branchName) {
		this.branchName = branchName;
	}

	
	
	
	
}
